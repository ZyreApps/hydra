//  ---------------------------------------------------------------------------
//  connect_to_server_endpoint
//

static void
connect_to_server_endpoint (client_t *self)
{
    if (zsock_connect (self->dealer, "%s", self->args->endpoint)) {
        engine_set_exception (self, error_event);
        zsys_warning ("could not connect to %s", self->args->endpoint);
    }
}


//  ---------------------------------------------------------------------------
//  use_connect_timeout
//

static void
use_connect_timeout (client_t *self)
{
    engine_set_timeout (self, self->args->timeout);
}


//  ---------------------------------------------------------------------------
//  prepare_to_send_get_tag
//

static void
prepare_to_send_get_tag (client_t *self)
{
    hydra_msg_set_tag (self->message, self->args->tag);
}


//  ---------------------------------------------------------------------------
//  prepare_to_send_get_post
//

static void
prepare_to_send_get_post (client_t *self)
{
    hydra_msg_set_post_id (self->message, self->args->post_id);
}


//  ---------------------------------------------------------------------------
//  return_tags_to_application
//

static void
return_tags_to_application (client_t *self)
{
    zsock_send (self->cmdpipe, "ss", "TAGS",
                 hydra_msg_tags (self->message));
}


//  ---------------------------------------------------------------------------
//  return_tag_to_application
//

static void
return_tag_to_application (client_t *self)
{
    zsock_send (self->cmdpipe, "ss", "TAG",
                 hydra_msg_tag (self->message),
                 hydra_msg_post_id (self->message));
}


//  ---------------------------------------------------------------------------
//  return_post_to_application
//

static void
return_post_to_application (client_t *self)
{
    zsock_send (self->cmdpipe, "ssssiss", "POST",
                 hydra_msg_post_id (self->message),
                 hydra_msg_reply_to (self->message),
                 hydra_msg_previous (self->message),
                 hydra_msg_tags (self->message),
                 (int) hydra_msg_timestamp (self->message),
                 hydra_msg_type (self->message),
                 hydra_msg_content (self->message));
}


//  ---------------------------------------------------------------------------
//  signal_success
//

static void
signal_success (client_t *self)
{
    zsock_send (self->cmdpipe, "si", "SUCCESS", 0);
}


//  ---------------------------------------------------------------------------
//  signal_server_not_present
//

static void
signal_server_not_present (client_t *self)
{
    zsock_send (self->cmdpipe, "sis", "FAILURE", -1, "Server is not reachable");
}


//  ---------------------------------------------------------------------------
//  signal_failure
//

static void
signal_failure (client_t *self)
{
    zsock_send (self->cmdpipe, "si", "FAILURE", -1);
}


//  ---------------------------------------------------------------------------
//  Selftest

void
hydra_client_test (bool verbose)
{
    printf (" * hydra_client: ");
    if (verbose)
        printf ("\n");
    
    //  @selftest
    //  Start a server to test against, and bind to endpoint
    zactor_t *server = zactor_new (hydra_server, "hydra_client_test");
    if (verbose)
        zstr_send (server, "VERBOSE");
    zstr_sendx (server, "BIND", "ipc://@/hydra", NULL);
    
    hydra_client_t *client = hydra_client_new ("ipc://@/hydra", 500);
    if (verbose)
        hydra_client_verbose (client);
    hydra_client_get_tags (client);
    hydra_client_destroy (&client);
    
    zactor_destroy (&server);
    //  @end
    printf ("OK\n");
}
